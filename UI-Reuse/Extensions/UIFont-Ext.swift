
import UIKit

extension UIFont {
    
    class func defaultFontRegular(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Regular", size: size)!
    }
    
    class func defaultFontMedium(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Medium", size: size)!
    }
    
    class func defaultFontLight(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Light", size: size)!
    }
    
    class func defaultFontDemi(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Demi", size: size)!
    }
    
    class func defaultFontBold(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Bold", size: size)!
    }
    
    class func defaultFontThin(size : CGFloat) -> UIFont {
        return UIFont(name: "AvenirNextLTPro-Thin", size: size)!
    }
}